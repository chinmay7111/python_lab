tuple1 = ('one ',2,3,4,'five',6)

print(tuple1)        # its print the whole tuple  and we can not change or modify the tuple

print(tuple1[2:4])    # its print the tuple from given index 

print(tuple1[3:])     # its prints all the tuple from given index no.

print(tuple1 * 2)      # its print the tuple form 2 times
